/**
 * Get translation for a key in a given language
 * @param {string} key - Translation key
 * @param {import('./types.js').Language} language - Language code
 * @param {Object<string, string>} [fallbacks] - Optional fallback translations
 * @returns {string} Translated string or key if not found
 */
export function getTranslation(key, language, fallbacks = translations) {
  const langTranslations = fallbacks[language] || fallbacks.en;
  return langTranslations[key] || key;
}
